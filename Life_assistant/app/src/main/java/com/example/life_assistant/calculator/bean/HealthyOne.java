package com.example.life_assistant.calculator.bean;

public class HealthyOne {
    private String idealWeight;
    private String normalWeight;
    private Integer level;
    private String levelMsg;
    private String danger;
    private String bmi;
    private String normalBMI;

    public String getIdealWeight() {
        return idealWeight;
    }

    public void setIdealWeight(String idealWeight) {
        this.idealWeight = idealWeight;
    }

    public String getNormalWeight() {
        return normalWeight;
    }

    public void setNormalWeight(String normalWeight) {
        this.normalWeight = normalWeight;
    }

    public Integer getLevel() {
        return level;
    }

    public void setLevel(Integer level) {
        this.level = level;
    }

    public String getLevelMsg() {
        return levelMsg;
    }

    public void setLevelMsg(String levelMsg) {
        this.levelMsg = levelMsg;
    }

    public String getDanger() {
        return danger;
    }

    public void setDanger(String danger) {
        this.danger = danger;
    }

    public String getBmi() {
        return bmi;
    }

    public void setBmi(String bmi) {
        this.bmi = bmi;
    }

    public String getNormalBMI() {
        return normalBMI;
    }

    public void setNormalBMI(String normalBMI) {
        this.normalBMI = normalBMI;
    }
}
